/**
 * Reads scripts/zappar-camera-init.js and app/(tabs)/zappar-camera.html, then writes
 * lib/zappar-camera-script.generated.ts so the camera WebView can use them without
 * embedding strings in TS. Output is in lib/ to avoid Expo Router treating it as a route.
 *
 * Run: node scripts/generate-zappar-script.js (or npm run generate:zappar-script)
 */
const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const SCRIPT_SRC = path.join(ROOT, "scripts", "zappar-camera-init.js");
const HTML_SRC = path.join(ROOT, "app", "(tabs)", "zappar-camera.html");
const OUT = path.join(ROOT, "lib", "zappar-camera-script.generated.ts");

const scriptContent = fs.readFileSync(SCRIPT_SRC, "utf8");
const htmlContent = fs.readFileSync(HTML_SRC, "utf8");

const scriptEscaped = JSON.stringify(scriptContent);
const htmlEscaped = JSON.stringify(htmlContent);

const output = `// Generated by scripts/generate-zappar-script.js — do not edit by hand.
export const ZAPPAR_CAMERA_SCRIPT = ${scriptEscaped};
export const ZAPPAR_HTML_SHELL = ${htmlEscaped};
`;

fs.writeFileSync(OUT, output, "utf8");
console.log("Wrote", path.relative(ROOT, OUT));
