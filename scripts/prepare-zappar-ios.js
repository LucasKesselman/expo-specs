#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..");
const sourcePath = process.env.ZAPPAR_IOS_EMBED_SOURCE;
const targetRoot = path.join(repoRoot, "zappar-artifacts", "ios");
const targetPath = path.join(targetRoot, "ZapparEmbed.include");

function fail(message) {
  console.error(`\n[prepare-zappar-ios] ${message}\n`);
  process.exit(1);
}

if (!sourcePath) {
  fail(
    "Missing ZAPPAR_IOS_EMBED_SOURCE. Point this to an unpacked ZapparEmbed.include directory.",
  );
}

const resolvedSourcePath = path.resolve(sourcePath);

if (!fs.existsSync(resolvedSourcePath)) {
  fail(`Source path does not exist: ${resolvedSourcePath}`);
}

const sourceStats = fs.statSync(resolvedSourcePath);
if (!sourceStats.isDirectory()) {
  fail(`Source path must be a directory: ${resolvedSourcePath}`);
}

if (!resolvedSourcePath.endsWith("ZapparEmbed.include")) {
  fail("Source directory must end with 'ZapparEmbed.include'.");
}

fs.mkdirSync(targetRoot, { recursive: true });
fs.rmSync(targetPath, { recursive: true, force: true });
fs.cpSync(resolvedSourcePath, targetPath, { recursive: true });

const markerPath = path.join(targetRoot, "README.txt");
const markerContents = [
  "Generated by scripts/prepare-zappar-ios.js",
  "This directory is intentionally gitignored.",
  "Do not commit Zappar vendor artifacts to source control.",
  "",
].join("\n");
fs.writeFileSync(markerPath, markerContents, "utf8");

console.log(`[prepare-zappar-ios] Copied ${resolvedSourcePath}`);
console.log(`[prepare-zappar-ios] Prepared ${targetPath}`);
